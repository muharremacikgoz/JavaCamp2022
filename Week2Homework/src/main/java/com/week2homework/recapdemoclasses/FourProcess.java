public class FourProcess {
    public int summation(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public int extraction(int sayi1, int sayi2) {
        return sayi1 - sayi2;
    }

    public int crash(int sayi1, int sayi2) {
        return sayi1 * sayi2;
    }

    public int episode(int sayi1, int sayi2) {
        return sayi1 / sayi2;
    }
}
